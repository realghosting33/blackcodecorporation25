function WeaponSkins:GetAllSkins()
    local allSkins = {}
    
    for weapon, skins in pairs(self) do
        if type(skins) == "table" and weapon ~= "GetAllSkins" and weapon ~= "HasWeapon" and weapon ~= "ApplySkin" then
            for _, skin in ipairs(skins) do
                table.insert(allSkins, {weapon .. "_" .. skin})
            end
        end
    end
    
    return allSkins
end

function WeaponSkins:HasWeapon(weaponName)
    return self[weaponName] ~= nil
end

